Berikut sudah disiapkan sebuah array dengan berbagai macam angka sebagai elements-nya. 
<br>
<br>
Ubahlah tiap-tiap element dari array tersebut menjadi boolean (true / false).
<br>
<br>
Kapan harus diubah menjadi true, dan kapan menjadi false?
<br>
Nah, jika bilangannya genap maka ubahlah menjadi true, <br>
dan apabila bilangannya ganjil maka ubahlah menjadi false.